$(function(){
	$("#content-main").click(function(){
		const pm = $(".pop_mode").css("display");
		if(pm != "none"){
			$(".pop_mode").fadeOut();
			$(".pop_box").fadeOut();
			$(".pop_img").fadeOut();
			$(".pop_txt").fadeOut();
			return false;
		}
	});
});

