$(function(){
	$("#gi0").click(function(){
		$(".pop_box").hide();
		$(".pop_img").hide();
		$(".pop_txt").hide();
		$(".pop_mode").fadeIn();
		$(".pb0").fadeIn();
		$(".pi0").fadeIn();
		$(".pt0").fadeIn();
		$(".slider").slick('slickGoTo', 0, false);
		$(".slider").slick('setPosition');
		return false;
	});
});
